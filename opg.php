                                <p class="mb-30">An OPG (Orthopantomagram) is a panoramic scanning dental X-ray of the upper and lower jaw. It is also sometimes called Orthopantomagraph or by the proprietary name Panorex. It shows a flattened two-dimensional view of a half-circle from ear to ear. Panoramic x-rays allow images of multiple angles to be taken to make up the composite panoramic image, where the maxilla (upper jaw) and mandible (lower jaw) are in the viewed area. The structures that are outside the viewed area are blurred. At some stage in your dental treatment, your dentist will likely take an OPG.</p>
                                <p>An OPG also demonstrates the number, position and growth of all the teeth including those that have not yet surfaced or erupted through the gum. It is different from the small close up x-rays dentists take of individual teeth. It shows less fine detail, but a much broader area of view. This can be particularly useful to check hard to see areas like wisdom teeth, or the development of a child’s jaw and teeth, useful for assessing for development generally, but also orthodontic need. It is also often used to check your jaw joint, the TMJ (temperomandibular joint), sometimes called the CMA (cranio-mandibular articulation), especially if you grind your teeth.</p>
