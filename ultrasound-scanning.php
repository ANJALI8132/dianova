                        <p><span>An ultrasound scan, sometimes called a sonogram, is a procedure that uses high-frequency sound waves to create an image of part of the inside of the body. An ultrasound scan can be used to monitor an unborn baby, diagnose a condition, or guide a surgeon during certain procedures.
                        An ultrasound scan uses high-frequency sound waves to create images of the inside of the body. It is suitable for use during pregnancy.
