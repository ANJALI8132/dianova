                        <p>Ultrasound scans, or sonography, are safe because they use sound waves or echoes to make an image, instead of radiation.
                        Ultrasound scans are used to evaluate fetal development, and they can detect problems in the liver, heart, kidney, or abdomen. They may also assist in performing certain types of biopsy.
                        The image produced is called a sonogram.

                        <p class="mb-3">Fast facts on ultrasound scans:</p>
                        <ul>
                            <li><i class="fas fa-angle-double-right"></i>   Ultrasound scans are safe and widely used.</li>
                            <li><i class="fas fa-angle-double-right"></i>	They are often used to check the progress of a pregnancy.</li>
                            <li><i class="fas fa-angle-double-right"></i>	They are used for diagnosis or treatment.</li>
                            <li><i class="fas fa-angle-double-right"></i>	No special preparation is normally necessary before an ultrasound scan.</li>

                            <li><i class="fas fa-angle-double-right"></i>    The person who performs an ultrasound scan is called a sonographer, but the images are interpreted by radiologists, cardiologists, or other specialists.</li>
                            <li><i class="fas fa-angle-double-right"></i>	The sonographer usually holds a transducer, a hand-held device, like a wand, which is placed on the patient’s skin.</li>
                            <li><i class="fas fa-angle-double-right"></i>   Ultrasound is sound that travels through soft tissue and fluids, but it bounces back, or echoes, off denser surfaces. This is how it creates an image.</li>
                            <li><i class="fas fa-angle-double-right"></i>	The term “ultrasound” refers to sound with a frequency that humans cannot hear.</li>
                            <li><i class="fas fa-angle-double-right"></i>	For diagnostic uses, the ultrasound is usually between 2 and 18 megahertzTrusted Source (MHz).</li>
                            <li><i class="fas fa-angle-double-right"></i>   Higher frequencies provide better quality images but are more readily absorbed by the skin and other tissue, so they cannot penetrate as deeply as lower frequencies</li>
                            <li><i class="fas fa-angle-double-right"></i>	Lower frequencies penetrate deeper, but the image quality is inferior.</li>
                        </ul>
                        </p>
                        <p class="mb-3">How does it capture an image?
                            <ul>
                            <li><i class="fas fa-angle-double-right"></i> Ultrasound will travel through blood in the heart chamber, for example, but if it hits a heart valve, it will echo, or bounce back.</li>
                            <li><i class="fas fa-angle-double-right"></i> 	It will travel straight through the gallbladder if there are no gallstones, but if there are stones, it will bounce back from them.</li>
                            <li><i class="fas fa-angle-double-right"></i> 	The denser the object the ultrasound hits, the more of the ultrasound bounces back.</li>
                            <li><i class="fas fa-angle-double-right"></i> 	This bouncing back, or echo, gives the ultrasound image its features. Varying shades of gray reflect different densities.</li>
                        </ul>
