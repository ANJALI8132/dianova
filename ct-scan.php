                            <h2>CT Scan</h2>
                            <div class="text">

                            <p><span>Medical professionals use computed tomography, also known as CT scan, to examine structures inside your body. A CT scan uses X-rays and computers to produce images of a cross-section of your body. It takes pictures that show very thin “slices” of your bones, muscles, organs and blood vessels so that healthcare providers can see your body in great detail.
                            Traditional X-ray machines use a fixed tube to point X-rays at a single spot. As X-rays travel through the body, they are absorbed in different amounts by different tissues. Higher density tissue create a whiter image than other tissues against the black background of the film. X-rays produce 2D images. CT scans have a doughnut-shaped tube that rotates the X-ray 360 degrees around you. The data captured provides a detailed 3D view of the inside of your body.</span></p>
                            <p>Your healthcare provider will order a CT scan to help make a diagnosis of your health. The scan enables providers to closely examine bones, organs and other soft tissues, blood vessels and suspicious growths. </p>
                            <p class="mb-3">Things that a CT scan can find include:</p>
	
<ul>
    <li><i class="fas fa-angle-double-right"></i>  	Certain types of cancer and benign (noncancerous) tumors.</li>
	<li><i class="fas fa-angle-double-right"></i>  	Fractures (broken bones).</li>
	<li><i class="fas fa-angle-double-right"></i>  	Heart disease.</li>
	<li><i class="fas fa-angle-double-right"></i>  	Blood clots.</li>
	<li><i class="fas fa-angle-double-right"></i>  	Bowel disorders (blockages, Crohn's disease).</li>
	<li><i class="fas fa-angle-double-right"></i>  	Brain and spinal cord diseases or injuries.</li>
	<li><i class="fas fa-angle-double-right"></i>  	Internal bleeding.</li>
</ul>
<p>
Healthcare providers can also see organs and tissues on X-rays. But on X-rays, body structures appear to overlap, making it difficult to see everything. The CT scan shows spaces between organs for a clearer view.
