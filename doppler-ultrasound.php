                            <h2>Doppler Ultrasound</h2>
                            <div class="text">

                            <p>What is a Doppler ultrasound?
                            A Doppler ultrasound is an imaging test that uses sound waves to show blood moving through blood vessels. A regular ultrasound also uses sound waves to create images of structures inside the body, but it can't show blood flow.
                            Doppler ultrasound works by measuring sound waves that are reflected from moving objects, such as red blood cells. This is known as the Doppler effect.
                            There are different types of Doppler ultrasound tests.</p>
                            <p class="mb-3">They include:</p>
                            <ul>
                                    <li><i class="fas fa-angle-double-right"></i><strong> Color Doppler:</strong> This type of Doppler uses a computer to change sound waves into different colors. These colors show the speed and direction of blood flow in real time.</li>
                                    <li><i class="fas fa-angle-double-right"></i><strong> Power Doppler:</strong> a newer type of color Doppler. It can provide more detail of blood flow than standard color Doppler. But it cannot show the direction of blood flow, which can be important in some cases.</li>
                                    <li><i class="fas fa-angle-double-right"></i> <strong>Spectral Doppler:</strong>  This test shows blood flow information on a graph, rather than color pictures. It can help show how much of a blood vessel is blocked.</li>
                                    <li><i class="fas fa-angle-double-right"></i> <strong>Duplex Doppler:</strong>  This test uses standard ultrasound to take images of blood vessels and organs. Then a computer turns the images into a graph, as in spectral Doppler.</li>
                                    
                                    <li><i class="fas fa-angle-double-right"></i><strong>Continuous wave Doppler:</strong>  In this test, sound waves are sent and received continuously. It allows for more accurate measurement of blood that flows at faster speeds.</li>
                            </ul>
                            <p>Other names: Doppler ultrasonography</p>
                            <h3 class="mt-3">What is it used for?</h3>
                            <p class="mt-3">Doppler ultrasound tests are used to help health care providers find out if you have a condition that is reducing or blocking your blood flow. It may also be used to help diagnose certain heart diseases. The test is most often used to:</p>
                                <ul>
                                    
                                <li>	Check heart function. It is often done along with an electrocardiogram, a test that measures electrical signals in the heart.</li>
                                <li>	Look for blockages in blood flow. Blocked blood flow in the legs can cause a condition called deep vein thrombosis (DVT).</li>
                                <li>	Check for blood vessel damage and for defects in the structure of the heart.</li>
                                <li>	Look for narrowing of blood vessels. Narrowed arteries in arms and legs can mean you have condition called peripheral arterial disease (PAD). Narrowing of arteries in the neck can mean you have a condition called carotid artery stenosis.</li>
                                <li>	Monitor blood flow after surgery.</li>
                            
                                <li>    Check for normal blood flow in a pregnant woman and her unborn baby.</li>
                            </ul>
                            </p>

