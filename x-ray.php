                            <div class="text">
                            <p>X-ray or radiography uses a very small dose of ionizing radiation to produce pictures of the body's internal structures. X-rays are the oldest and most frequently used form of medical imaging. They are often used to help diagnosed fractured bones, look for injury or infection and to locate foreign objects in soft tissue. Some x-ray exams may use an iodine-based contrast material or barium to help improve the visibility of specific organs, blood vessels, tissues or bone.</p>

    
                                <p class="mb-3">Medical radiography is a broad term that covers several types of studies that require the visualization of the internal parts of the body using x-ray techniques. For the purposes of this page radiography means a technique for generating and recording an x-ray pattern for the purpose of providing the user with a static image(s) after termination of the exposure.. Radiography may also be used during the planning of radiation therapy treatment. (links are to the pages in this section)</p>
                                <p class="mb-3">It is used to diagnose or treat patients by recording images of the internal structure of the body to assess the presence or absence of disease, foreign objects, and structural damage or anomaly.</p>
                                <p>During a radiographic procedure, an x-ray beam is passed through the body. A portion of the x-rays are absorbed or scattered by the internal structure and the remaining x-ray pattern is transmitted to a detector so that an image may be recorded for later evaluation. The recoding of the pattern may occur on film or through electronic means. </p>
                                
